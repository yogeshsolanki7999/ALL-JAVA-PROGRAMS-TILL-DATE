package com.Harry;

public class y12_increament_and_decreament_operator {
    public static void main(String[] args) {
        int i=5;
        int j=i++;
        System.out.println(j);
        int ii=5;
        int jj=++ii;
        System.out.println(jj);
        int a=55;
        int b=100;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(b);
        System.out.println(++b);
        System.out.println(b);


    }
}
