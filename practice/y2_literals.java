package com.practice;

public class y2_literals {
    public static void main(String [] args){
        int a=10;// 2 byte, default 0
        byte b=10;// 1 byte, default 0
        short c=10;// byte, default 0
        long d=10;// 8 byte, default 0
        float e=1.0f;// 4 byte, default 0.0f
        double f=100.0d;// 8 byte, default 0.0d
        char g='d';// 2 byte, default '/00000'
        String h="yogesh solanki";// class
        boolean i=true;         // size depend on jvm default is false
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
    }
}
